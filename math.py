def add(num1,num2):
    answer = num1+num2
    return answer

def subtract(num1,num2):
    answer=num1-num2
    return answer 

def divide(num1,num2):
    answer = num1/num2
    return answer

def multiply(num1,num2):
    answer = num1*num2
    return answer

def remainder(num1,num2):
    answer = num1%num2
    return answer               
